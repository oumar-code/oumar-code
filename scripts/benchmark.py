#!/usr/bin/env python3
"""
benchmark.py — run evaluation for every challenge × profile combination
and emit a consolidated markdown + JSON report.
"""
from __future__ import annotations

import json
import sys
from dataclasses import asdict
from pathlib import Path

from core.evaluation.evaluator import run_eval, save_results

CHALLENGES = ["arm-create", "backblaze-genblaze", "qwen-cloud", "demo"]
PROFILES = ["safe", "aggressive"]


def build_markdown_table(rows: list[dict]) -> str:
    headers = [
        "challenge", "profile", "model", "total_samples",
        "quality_score", "avg_latency_ms", "cost_per_run", "reliability",
    ]
    sep = "| " + " | ".join(["---"] * len(headers)) + " |"
    header_row = "| " + " | ".join(headers) + " |"
    lines = [header_row, sep]
    for r in rows:
        line = "| " + " | ".join(str(r.get(h, "")) for h in headers) + " |"
        lines.append(line)
    return "\n".join(lines)


def main() -> None:
    challenges = sys.argv[1:] or CHALLENGES
    all_rows: list[dict] = []

    for challenge in challenges:
        for profile in PROFILES:
            print(f"\n[benchmark] {challenge} / {profile}")
            try:
                result = run_eval(challenge=challenge, profile=profile)
                save_results(result)
                row = {k: v for k, v in asdict(result).items() if k != "raw"}
                all_rows.append(row)
            except Exception as exc:  # noqa: BLE001
                print(f"  [skip] {exc}")

    if not all_rows:
        print("[benchmark] No results collected.")
        return

    # Write consolidated report
    report_json = Path("benchmarks") / "report.json"
    report_md = Path("benchmarks") / "report.md"

    report_json.write_text(json.dumps(all_rows, indent=2))

    md_content = "# Benchmark Report\n\n"
    md_content += build_markdown_table(all_rows)
    md_content += "\n\n> Generated by `scripts/benchmark.py`\n"
    report_md.write_text(md_content)

    print(f"\n[benchmark] Report → {report_json}")
    print(f"[benchmark] Report → {report_md}")
    print(f"\n{md_content}")


if __name__ == "__main__":
    main()
